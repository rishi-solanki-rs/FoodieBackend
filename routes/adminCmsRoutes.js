const express = require('express');
const router = express.Router();
const privacyPolicyController = require('../controllers/privacyPolicyController');
const termsConditionsController = require('../controllers/termsConditionsController');
const aboutUsController = require('../controllers/aboutUsController');
const faqController = require('../controllers/faqController');
const contactFormController = require('../controllers/contactFormController');
const { protect, admin } = require('../middleware/authMiddleware');
router.get('/privacy-policy', protect, admin, privacyPolicyController.getPrivacyPolicyAdmin);
router.post('/privacy-policy', protect, admin, privacyPolicyController.updatePrivacyPolicy);
router.put('/privacy-policy', protect, admin, privacyPolicyController.updatePrivacyPolicy);
router.get('/terms-conditions', protect, admin, termsConditionsController.getTermsConditionsAdmin);
router.post('/terms-conditions', protect, admin, termsConditionsController.updateTermsConditions);
router.put('/terms-conditions', protect, admin, termsConditionsController.updateTermsConditions);
router.get('/about-us', protect, admin, aboutUsController.getAboutUsAdmin);
router.post('/about-us', protect, admin, aboutUsController.updateAboutUs);
router.put('/about-us', protect, admin, aboutUsController.updateAboutUs);
router.get('/faq', protect, admin, faqController.getAllFAQsAdmin);
router.post('/faq', protect, admin, faqController.createFAQ);
router.put('/faq/:id', protect, admin, faqController.updateFAQ);
router.delete('/faq/:id', protect, admin, faqController.deleteFAQ);
router.put('/faq/reorder', protect, admin, faqController.reorderFAQs);
router.get('/contact', protect, admin, contactFormController.getAllContacts);
router.get('/contact/stats', protect, admin, contactFormController.getContactStats);
router.get('/contact/:id', protect, admin, contactFormController.getContactById);
router.put('/contact/:id/reply', protect, admin, contactFormController.replyContact);
router.put('/contact/:id/close', protect, admin, contactFormController.closeContact);
router.delete('/contact/:id', protect, admin, contactFormController.deleteContact);
module.exports = router;
